//设置LCD参数
//方便进行横竖屏模式切换
void LCD_SetParam(void)
{ 	
	lcddev.wramcmd=0x2C;
#if USE_HORIZONTAL==1	//使用横屏	  
	lcddev.dir=1;//横屏
	lcddev.width=128;
	lcddev.height=128;
	lcddev.setxcmd=0x2A;
	lcddev.setycmd=0x2B;			
	LCD_WriteReg(0x36,0xA8);

#else//竖屏
	lcddev.dir=0;//竖屏				 	 		
	lcddev.width=128;
	lcddev.height=128;
	lcddev.setxcmd=0x2A;
	lcddev.setycmd=0x2B;	
	LCD_WriteReg(0x36,0xC8);
#endif
}


void Gui_Drawbmp16(u16 x,u16 y,u16 w,u16 h,const unsigned char *p) //显示w*h 图片
{
  	int i,j; 
	unsigned char picH,picL; 
	LCD_SetWindows(x,y,x+w-1,y+h-1);//窗口设置
	j=w*h;
  for(i=0;i<j;i++)
	{	
	 	picL=*(p+i*2);	//数据低位在前
		picH=*(p+i*2+1);				
		LCD_WR_DATA_16Bit(picH<<8|picL);  						
	}	
	LCD_SetWindows(0,0,lcddev.width-1,lcddev.height-1);//恢复显示窗口为全屏	

}
//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 
//size:选择字体 16/12 
void LCD_ShowChar(u8 x,u8 y,u8 chr)
{      	
	unsigned char c=0;	
	c=chr-' ';//得到偏移后的值
	LCD_SetWindows(x,y,x+7,y+15);//窗口设置
	for(int i=7;i>=0;i--)
	{
		for(int j=0;j<8;j++)
		{
			if((F8X16[c*16+j]<<i)&0x80)
			 LCD_WR_DATA_16Bit(GRAYBLUE);
			else
			 LCD_WR_DATA_16Bit(WHITE);	
		}
	}
	for(int i=7;i>=0;i--)
	{
		for(int j=0;j<8;j++)
		{
			if((F8X16[c*16+j+8]<<i)&0x80)
			 LCD_WR_DATA_16Bit(GRAYBLUE);
			else
			 LCD_WR_DATA_16Bit(WHITE);	
		}
	}
	LCD_SetWindows(0,0,lcddev.width-1,lcddev.height-1);//恢复显示窗口为全屏	
}
//显示一个字符号串
void LCD_ShowString(u8 x,u8 y,char *chr)
{
	unsigned char j=0;
	while (chr[j]!='\0')
	{	
		LCD_ShowChar(x,y,chr[j]);
			x+=8;
		if(x>120){x=0;y+=2;}
			j++;
	}
}
   
void LCD_show_float(u16 x,u16 y,double dat)
{
	long long int temp[6];
	int X,Y;
	X=x;Y=y;
	char flag_np=0;
	if(dat<0)
	{
		flag_np=1;
		dat=-dat;

	}
	for(int i=5;i>=0;i--)
	{
		temp[i]=((long long int)dat%(long long int)(pow(10,i+1)))/(long long int)(pow(10,i));
	}
	char flag=0;
	for(int i=5;i>=0;i--)
	{
		if(flag==0)
		{
			if(temp[i]==0)
			{
				if(i==0)
				{
					LCD_ShowChar(X+=8,Y,'0');
				}
				else
				{
					LCD_ShowChar(X+=8,Y,' ');  
				}
			}
			
			else 
			{
				if(flag_np==1)
				LCD_ShowChar(X+=8,Y,'-');
				LCD_ShowChar(X+=8,Y,((char)(temp[i])+48));
				flag=1;
			}
		}
		else
		LCD_ShowChar(X+=8,Y,((char)(temp[i])+48));
	}
	X=X+8;
	LCD_ShowChar(X,Y,'.');

	dat=(dat-(long long int)dat)*pow(10,6);
	for(int i=5;i>=0;i--)
	{
		temp[i]=((long long int)dat%(long long int)(pow(10,i+1)))/(long long int)(pow(10,i));
	}
	for(int i=5;i>=0;i--)
	{
		LCD_ShowChar(X+=8,Y,((char)(temp[i])+48));
	}
}


